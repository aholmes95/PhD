function F = solveMC(m,n,m2,mn,n2,m3,m2n,mn2,n3)
    a = 0.1;
    b = 0.1;
    g = 0.15;
    N = 1500;
    F(1) = -2*a*m+g*n+2*a/N*m2+a/N*mn;
    F(2) = (2*a-2*g)*m+(-a-b-3*g)*n-2*a/N*m2+a/(2*N)*n2+2*g*N;
    F(3) = 2*a*m+g*n+(-4*a-2*a/N)*m2+(2*g-a/N)*mn+2*a/N*m2n+4*a/N*m3;
    F(4) = (2*a-2*g)*a*m+(4*a-4*g-2*a/N)*mn+(-2*a-2*b-6*g/(2*N))*n2-2*a/N*m2-4*a/N*m2n+a/N*m3+(a+b-g+4*g*N)*n;
    F(5) = -g*a*n+g*n2+(-3*a-b-3*g+a/N)*mn-(2*a-2*g+2*a/N)*m2+2*a/N*m2n-2*a/N*m3+3*a/(2*N)*mn2+(2*g*N-2*a)*m;
    F(6) = m3-3*m2*m+2*m^3;
    F(7) = n3-3*n2*n+2*n^3;
    F(8) = m2n-m2*n-2*mn*m+2*m^2*n;
    F(9) = mn2-m*n2-2*mn*n+2*m*n^2;